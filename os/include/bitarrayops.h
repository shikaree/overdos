/* This is functions that allows you to use an array of unsigned chars 
   array of bits */

/* Use setbit to set bit n in the imagined bitarray  to 1*/

void setbit(unsigned char[], int);

/* Use resetbit to set bit n in the imagined bitarray to 0*/

void resetbit(unsigned char[], int);

/* Use getbit to get the value, 0 or 1, of bit n in the imagined 
   bitarray */

unsigned char getbit(unsigned char[], int);
